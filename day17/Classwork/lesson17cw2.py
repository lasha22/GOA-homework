text = "goa is best"
uppercase_text = text.upper()
print(uppercase_text)  


text = "GOA IS BEST"
lowercase_text = text.lower()
print(lowercase_text)  


text = "GOA IS BEST"
capitalized_text = text.capitalize()
print(capitalized_text) 


words = ["Goa", "is", "best"]
joined_text = " ".join(words)
print(joined_text)  


